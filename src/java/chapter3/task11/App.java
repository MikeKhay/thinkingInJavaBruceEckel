package chapter3.task11;

public class App {
    public static void main(String[] args) {

        int num = 14;
        System.out.println("Number      = " + Integer.toBinaryString(num));
        System.out.println("Number >> 1 = " + Integer.toBinaryString(num >> 1));
        System.out.println("Number >> 2 = " + Integer.toBinaryString(num >> 2));
        System.out.println("Number >> 3 = " + Integer.toBinaryString(num >> 3));
        System.out.println("Number >> 4 = " + Integer.toBinaryString(num >> 4));



    }
}
